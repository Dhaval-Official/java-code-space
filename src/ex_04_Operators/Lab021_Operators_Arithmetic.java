package ex_04_Operators;

public class Lab021_Operators_Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a%b);
        System.out.println("a+b");

    }
}

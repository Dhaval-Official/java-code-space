package ex_02_Java_Basics;

public class Lab011_Prints_Compare {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a);
        System.out.println(b);

        System.out.print(a);
        System.out.print(b);

        System.out.println();

        System.out.printf("%d",a);
        System.out.printf("%d",b);
        /*
        * int, byte, long, short ---> %d
        * String -------------------> %s
        * float, double ------------> %f
        * boolean ------------------> %b
        * */
    }




}
